# Em Python, os operadores aritméticos são utilizados para realizar operações matemáticas.

# Os operadores aritméticos são:
# + (adição)
# - (subtração)
# * (multiplicação)
# / (divisão)
# % (módulo)
# ** (exponenciação)

# Exemplos:

# Adição
print(2+2) # 4

# Subtração
print(5-3) # 2

# Multiplicação
print(3*3) # 9

# Divisão
print(10/2) # 5.0

# Módulo (resto da divisão)
print(10%3) # 1

# Exponenciação (potenciação)
print(2**3) # 8