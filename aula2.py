# Tipos de dados:
# int -> números inteiros;
# float -> números de ponto flutuante (números com virgula/
# números quebrados);
# str -> tipo string;
# bool -> tipo verdadeiro ou falso;

#Tipo inteiro (int)
print(type(1))

#Tipo ponto flutuante (float)
print(type(12.0))

#Tipo string (str)
print(type("Hello World!"))

#Tipo booleano (bool)
print(type(False))
print(type(True))