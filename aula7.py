# Em Python, os operadores de comparação são utilizados para comparar dois valores.

# Os operadores de comparação são:
# == (igual)
# != (diferente)
# > (maior que)
# < (menor que)
# >= (maior ou igual a)
# <= (menor ou igual a)

# Quando feito a comparação, o computador irá dizer se a comparação é verdadeira (True) ou falsa (False).

# Exemplos:

# Igual
print(2 == 2) # True
print(2 == 3) # False

# Diferente
print(2 != 2) # False
print(2 != 3) # True

# Maior que
print(5 > 3) # True
print(3 > 5) # False

# Menor que
print(3 < 5) # True
print(5 < 3) # False

# Maior ou igual a
print(3 >= 3) # True
print(3 >= 2) # True

# Menor ou igual a
print(3 <= 3) # True
print(3 <= 4) # True