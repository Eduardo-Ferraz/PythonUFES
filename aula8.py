# Em Python, os operadores de atribuição são utilizados para atribuir valores a variáveis.

# Os operadores de atribuição são:
# = (atribuição)
# += (adição)
# -= (subtração)
# *= (multiplicação)
# /= (divisão)
# %= (módulo)

# Exemplos:

# Atribuição
a = 2
print(a) # 2

# Adição (ele pega o valor definido em a anteriormente e soma com 2)
a += 2
print(a) # 4

# Subtração (ele pega o valor definido em a anteriormente e subtrai 2)
a -= 2
print(a) # 2

# Multiplicação (ele pega o valor definido em a anteriormente e multiplica por 2)
a *= 2
print(a) # 4

# Divisão (ele pega o valor definido em a anteriormente e divide por 2)
a /= 2
print(a) # 2.0

# Módulo (ele pega o valor definido em a anteriormente e pega o resto da divisão por 2)
a %= 2
print(a) # 0.0